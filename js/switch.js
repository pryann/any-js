const grade = 4

switch (grade) {
  case 5:
    console.log('Jeles')
    break
  case 4:
    console.log('Jó')
    break
  case 3:
    console.log('Közepes')
    break
  case 2:
    console.log('Elégséges')
    break
  case 1:
    console.log('Elégtelen')
    break
  default:
    console.log('Nem értékelhető')
}
